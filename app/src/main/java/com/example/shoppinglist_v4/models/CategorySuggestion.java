package com.example.shoppinglist_v4.models;

public class CategorySuggestion {

    private String Id;
    private String Name;

    public CategorySuggestion(String id, String name) {
        Id = id;
        Name = name;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
